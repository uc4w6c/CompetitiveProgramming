#include <bits/stdc++.h>
using namespace std;

// E - Dividing Chocolate
// ちょっとわからなかった・・・全探索だと重そう
#define H_MAX 10
#define W_MAX 1000

int H, W, K;
int A[H_MAX][W_MAX];

int main() {
    cin >> H >> W >> K;
    for (int i = 0; i < H; i++) {
        for (int j = 0; j < W; j++) {
            cin >> A[i][j];
        }
    }




}
